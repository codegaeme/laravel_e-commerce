                                body: formData
                            })
                            .then(res => res.json())
                            .then(res => {
                                console.log(res);


                                if (res.success) {
                                    document.getElementById('variantPrice').textContent = res
                                        .data.price + ' đ';
                                    document.getElementById('variantQuantity').textContent =
                                        'Còn lại: ' + res.data.quantity;
                                    const newImageUrl = res.data.image ??
                                        '/images/no-image.png';
                                    document.getElementById('variantPriceInput').value = res
                                        .data.price;
                                    document.getElementById('variantQuantityInput').value = res
                                        .data.quantity;
                                    document.getElementById('variantIdInput').value = res
                                        .data.id_variant;


