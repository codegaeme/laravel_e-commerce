                    const allSelected = [...inputs].every(i => i.value !== '');
                    if (allSelected) {
                        const formData = new FormData(form);

                        fetch("{{ route('findVariant') }}", {
                                method: 'POST',
                                headers: {
                                    'X-CSRF-TOKEN': '{{ csrf_token() }}'
